function errCALL(message);

%%
%%	ERRCALL.M	Puts Up Box To Report Error and Waits for Button Press
%%
%%

ERR=figure(		'Name',	'ERROR', ...
			'NumberTitle', 'off', ...
			'Color', [1 1 1], ...
			'Units', 'normal',...
			'Position', [.35 .35 .30 .30]);			

ERRaxes=axes(		'Units', 'normal',...
			'Position',[.05 .25 .90 .70],...
			'Visible','off');

ERRmsg=text(		.5,.5, ...
			message,...
			'Color',[0 0 0],...
			'HorizontalAlignment','center',...
			'VerticalAlignment','middle');


ERRbut=uicontrol(	ERR,...
			'Style','pushbutton', ...
			'Units','normalized',...
			'Position',[.1 .05 .8 .20],...
			'Callback','delete(gcf)',...
			'String','Press to Continue',...
			'HorizontalAlignment','center');

