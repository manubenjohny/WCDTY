function Finput(message,initval);

%%
%%	FINPUT.M	Puts Up Box To Ask Question, and Gets Input
%%
%%

global HANDLE;
global answer;

HANDLE(1)=figure(	'Name',	'Get Input', ...
			'NumberTitle', 'off', ...
			'Color', [1 1 1], ...
			'Units', 'normal',...
			'Position', [.35 .35 .30 .30]);			

haxes=axes(		'Units', 'normal',...
			'Position',[.05 .25 .90 .70],...
			'Visible','off');

hmsg=text(		.5,.5, ...
			message,...
			'Color',[0 0 0],...
			'HorizontalAlignment','center',...
			'VerticalAlignment','middle');


HANDLE(2)=uicontrol(	HANDLE(1),...
			'Style','edit', ...
			'Units','normalized',...
			'Position',[.2 .05 .6 .10],...
			'Callback','answer = get(HANDLE(2),''String'');,delete(HANDLE(1))',...
			'String',initval,...
			'HorizontalAlignment','center',...
			'Max',1,...
			'Min',1);